package TypeCasting;

public class PrimitiveExample1 {

	public static void main(String[] args) {
		int i1=10;
		double d1=i1;
		System.out.println("Value of i1 is : " + i1);
		System.out.println("Value of d1 is : " + d1);
		System.out.println("==============================");
		double d2=10.6;
		int i2=(int)d2;
		System.out.println("Value of d2 is : " + d2);
		System.out.println("Value of i2 is : " + i2);
		System.out.println("==============================");
		double l2=10.7;
		long m2= (long) l2;
		System.out.println("Value of l2 is : " + l2);
		System.out.println("Value of m2 is : " + m2);
		System.out.println("==============================");
		byte b1=12 ;
		short s1=b1;
		System.out.println("Value of b1 is : " + b1);
		System.out.println("Value of s1 is : " + s1);
		System.out.println("==============================");
		int q1=10;
		float f1=i1;
		System.out.println("Value of q1 is : " + q1);
		System.out.println("Value of f1 is : " + f1);
		System.out.println("==============================");
		long l1=11;
		double d3=l1;
		System.out.println("Value of l1 is : " + l1);
		System.out.println("Value of d3 is : " + d3);
		System.out.println("==============================");
		double e1=11.9;
		long c3=(long)e1;
		System.out.println("Value of e1 is : " + e1);
		System.out.println("Value of c3 is : " + c3);
		System.out.println("==============================");
		double g4=12.7;
		float d4=(float)g4;
		System.out.println("Value of l1 is : " + g4);
		System.out.println("Value of d3 is : " + d4);
		System.out.println("==============================");

	}

}
